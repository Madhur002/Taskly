import mongoose from "mongoose";
import { Schema } from "mongoose";

const TodosSchema = new Schema ({
    user:{
        type: mongoose.Schema.Types.ObjectId,
        ref: 'user'
    },
    title: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    tag: {
        type: String,
        default: "General",
    },
    date: {
        type: Date,
        default: Date.now,
    },
});

export default mongoose.model('todos', TodosSchema);